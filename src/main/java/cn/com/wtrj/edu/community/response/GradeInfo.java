package cn.com.wtrj.edu.community.response;

public class GradeInfo {
	private String schoolCode;
	
	private Integer stage;
	
	private Integer grade;
	
	private String dispayName;
	
	private String gradeNo;
	
	private String gradeKeyNo;

	public String getSchoolCode() {
		return schoolCode;
	}

	public void setSchoolCode(String schoolCode) {
		this.schoolCode = schoolCode;
	}

	public Integer getStage() {
		return stage;
	}

	public void setStage(Integer stage) {
		this.stage = stage;
	}

	public Integer getGrade() {
		return grade;
	}

	public void setGrade(Integer grade) {
		this.grade = grade;
	}

	public String getDispayName() {
		return dispayName;
	}

	public void setDispayName(String dispayName) {
		this.dispayName = dispayName;
	}

	public String getGradeNo() {
		return gradeNo;
	}

	public void setGradeNo(String gradeNo) {
		this.gradeNo = gradeNo;
	}

	public String getGradeKeyNo() {
		return gradeKeyNo;
	}

	public void setGradeKeyNo(String gradeKeyNo) {
		this.gradeKeyNo = gradeKeyNo;
	}
	
}
